    "충남 천안", "전북 군산", "전북 김제", "전북 남원", "전북 익산",
    "전북 전주", "전북 정읍", "전남 광양", "전남 나주", "전남 목포",
    "전남 순천", "전남 여수", "경북 경산", "경북 경주", "경북 구미",
    "경북 김천", "경북 문경", "경북 상주", "경북 안동", "경북 영주",
